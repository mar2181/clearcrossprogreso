// Helper to get prices with procedure details for a provider
export function getProviderPricesWithProcedures(providerId: string) {
  return providerPrices
    .filter((pp) => pp.provider_id === providerId)
    .map((pp) => {
      const procedure = procedures.find((p) => p.id === pp.procedure_id);
      return {
        ...pp,
        procedure: procedure
          ? { id: procedure.id, name: procedure.name, sort_order: procedure.sort_order, slug: procedure.slug }
          : null,
      };
    });
}

// Helper to get providers for a category
export function getProvidersByCategory(categorySlug: string) {
  const category = categories.find((c) => c.slug === categorySlug);
  if (!category) return [];

  return providers
    .filter((p) => p.category_id === category.id && p.verified)
    .map((p) => ({
      ...p,
      provider_prices: getProviderPricesWithProcedures(p.id),
    }))
    .sort((a, b) => {
      if (a.featured && !b.featured) return -1;
      if (!a.featured && b.featured) return 1;
      return (b.avg_rating || 0) - (a.avg_rating || 0);
    });
}

// Helper to get procedures for a category
export function getProceduresByCategory(categorySlug: string) {
  const category = categories.find((c) => c.slug === categorySlug);
  if (!category) return [];

  return procedures
    .filter((p) => p.category_id === category.id)
    .sort((a, b) => a.sort_order - b.sort_order);
}

// Helper to get reviews for a provider
export function getReviewsByProvider(providerId: string) {
  return reviews
    .filter((r) => r.provider_id === providerId && r.verified)
    .sort((a, b) => new Date(b.created_at).getTime() - new Date(a.created_at).getTime());
}

